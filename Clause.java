
public class Clause {
	
	private int a;
	private int b;
	private int c;
	
	
	public Clause(int a, int b, int c){
		this.a = a;
		this.b = b;
		this.c = c;
	}
	
	public void setA(int i){
		this.a = i;
	}
	
	public void setB(int i){
		this.b = i;
	}
	
	public void setC(int i){
		this.c = i;
	}
	
	public int getA(){
		return a;
	}
	
	public int getB(){
		return b;
	}
	
	public int getC(){
		return c;
	}

}
